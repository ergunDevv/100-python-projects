# The print() function prints whatever is inside the parenthesis to the console.
print('Hello World')
# String concatenation can be done with a plus sign
print('Hello ' + 'World!!')

# input() will get user input in console
print('Hello ' + input('What is your name?') + '!')

# len() function gets the string and returns the length of the string.
print(len('Hello'))

# In python variables can define directly(without any type. Python understands the what type of variable you define )
# Like favNumber=5 or myDogsName='Nymeria'
lengthOfTheString = len('Hello')
print(lengthOfTheString)
